package com.mkpits.java.methodprograms;

public class ExOfUserDefineMethod {
    public static void findEvenOdd(int num)
    {
//method body
        if(num%2==0)
            System.out.println(num+" is even");
        else
            System.out.println(num+" is odd");
    }
}
