//A program of Compute Power of Negative Number
package com.mkpits.java.loops;

public class ExOfPowerOfNegativeNumberUsingPowMethod {
    public static void main(String[] args) {

        // negative number
        int base = -3, exponent = 2;

        double result = Math.pow(base, exponent);
        System.out.println("Answer = " + result);
    }
}

